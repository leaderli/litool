package io.leaderli.litool.runner;

public class LongExpression {

    private String expr;

    public LongExpression() {
        this.expr = "";
    }

    public LongExpression(String expr) {
        this.expr = expr;
    }

    public String getExpr() {
        return expr;
    }

    public void setExpr(String expr) {
        this.expr = expr;
    }
}
